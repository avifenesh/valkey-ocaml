let test_non_mtls_constructors_have_no_client_cert () =
  (* The pre-existing constructors (insecure, with_ca_cert,
     with_system_cas) must leave client_certificates = `None
     so [wrap_with_tls] doesn't accidentally enable mTLS. *)
  let t1 = T.insecure () in
  Alcotest.(check bool) "insecure() -> no client cert"
    true (T.client_certificates t1 = `None);
  let t2 = T.with_ca_cert ~ca_pem:(fixture "ca.crt") () in
  Alcotest.(check bool) "with_ca_cert() -> no client cert"
    true (T.client_certificates t2 = `None)

let tests =
  [ Alcotest.test_case "with_client_cert parses valid PEM" `Quick
      test_parses_valid_mtls_pair;
    Alcotest.test_case "with_client_cert rejects garbage CA"
      `Quick test_rejects_garbage_ca;
    Alcotest.test_case "with_client_cert rejects garbage cert"
      `Quick test_rejects_garbage_client_cert;
    Alcotest.test_case "with_client_cert rejects garbage key"
      `Quick test_rejects_garbage_client_key;
    Alcotest.test_case "non-mTLS constructors have no client cert"
      `Quick test_non_mtls_constructors_have_no_client_cert;
  ]
